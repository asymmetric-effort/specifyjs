/**
 * Simplified world country/continent outline data for orthographic globe
 * projection. Each country is defined by one or more polygon rings of
 * [longitude, latitude] coordinate pairs.
 *
 * Data is intentionally simplified (fewer vertices) to keep bundle size
 * reasonable while still producing recognizable continental shapes.
 */

